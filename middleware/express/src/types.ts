export interface Owner {
  id: string;
  type: "user" | "organization";
  name: string;
  email?: string;
  capabilities: CapabilityId[];
  created_at: string;
  updated_at: string;
}

export interface User extends Owner {
  type: "user";
  email: string;
  first_name?: string;
  last_name?: string;
  avatar_url?: string;
}

export interface Organization extends Owner {
  type: "organization";
  domain?: string;
  website?: string;
  logo_url?: string;
  members: string[]; // User IDs
  admins: string[]; // User IDs
}
